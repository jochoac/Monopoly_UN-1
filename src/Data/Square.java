package Data;

/**
 *
 * @author Juan Ochoa
 */
public class Square {
    private int row;
    private int column;
    private String name;
    
    public Square(int row, int col, String name) {
        this.row = row;
        this.column = col;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
